    <h3>1.1.4 Registry Permissions</h3>

    <p>
        Settings in the registry are secured on a value-by-value basis. Permissions determine who can read, edit, delete
        settings and who can determine permissions changes on settings. This demonstrates that the registry is more than
        just a long list of values.
    </p>

    <img src="/assets/img/windows_security_basics/windows_components/registry_permissions.png" alt="" title="">

    <p>
        In the next chapter, we will learn more about permissions in the file system, allowing us to determine who can
        do what with files and folders. The same principles and mechanisms are used for registry values.
    </p>
