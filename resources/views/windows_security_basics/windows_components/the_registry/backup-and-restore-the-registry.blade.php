    <h3>1.1.3 Backup and Restore the Registry</h3>

    <p>Options for performing a backup and restore of the registry:</p>
    <ul>
        <li><strong>Creating a restore point:</strong> Use the start menu and type in "create restore point".</li>
    </ul>

    <img src="/assets/img/windows_security_basics/windows_components/backup_and_restore_the_registry.png" alt="" title="">

    <ul>
        <li><strong>Manually do a backup using "regedit.exe":</strong> Open the registry editor, go to "file", and
            select "export" in the menu.
        </li>
    </ul>
