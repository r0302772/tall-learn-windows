    <h3>1.2.1 Workgroup Model</h3>

    <p>Workgroup environments are the default setting for Windows client computers. Each computer is a member of a workgroup (defaulted to "Workgroup"). In this model:</p>

    <ul>
        <li>Every user creates an account during installation, stored in the User Account Database of that specific machine.</li>
        <li>Each computer has its own User Account Database, resulting in separate databases for each machine in the workgroup.</li>
        <li>Computers in the workgroup don't share or copy information from their databases to other workgroup members.</li>
        <li>Users must have separate accounts on each computer they want to access.</li>
        <li>Single Sign-On is challenging, and users may need to remember different passwords on different computers.</li>
    </ul>
