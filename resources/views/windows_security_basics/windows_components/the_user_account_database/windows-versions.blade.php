    <h5>1.2.3 Windows Versions and Licensing</h5>

    <p><strong>Licensing Types:</strong></p>

    <ol>
        <li><strong>Retail:</strong> Also known as Full Packaged Product (FPP), this license, commonly referred to as a
            "boxed copy," is purchased from technology stores or Windows online stores. It comes with one license key
            (product key), and the key can be used on another computer.
        </li>
        <li><strong>OEM (Original Equipment Manufacturer):</strong> Provided by the system manufacturer, this license
            type does not have a separate license key, and it cannot be used on another computer. For Windows 8 or
            Windows 10, the license key is embedded in the UEFI firmware chip.
        </li>
        <li><strong>Volume:</strong> This institutional license type is for organizations like schools and government
            offices. These non-resale licenses allow Windows activation on multiple computers with a single license key.
        </li>
    </ol>

