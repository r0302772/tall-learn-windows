    <h4>3.4.7 Key combination for logging in</h4>
    <p>
        In Virtual Box, you have to use different key combinations than those you use in Windows. They are usually
        combinations of the "host key" and another key. The host key is the default key ALT GR or CTRL to the right of
        the space bar. (On some keyboards, this is not available, and you will have to enter the host key in Virtual Box
        yourself.
    </p>
    <p>
        The "Host key" can be found in Virtual Box under "display" in the main menu. You will also find some key
        combinations to adjust the size of your screen. To log in, you will need the host key + DEL instead of ALT +
        CTRL + DEL.
    </p>

