    <h4>3.4.3 Visual Studio Code</h4>
    <p>
        Since our VMs still have an internet connection, we can install the PowerShell scripts editor on the Windows
        Server VM. The old editor that is installed with PowerShell is "deprecated," and we won't use it. Instead, we
        use "Visual Studio Code."
    </p>

    <p>
        On the server version, you cannot just download and install software. By default, your system is protected so
        that this is not possible. We will first disable this protection, so we can do the download through Internet
        Explorer.
    </p>

    <p>
        In your Windows Server VM, open the "Server Manager." Select "Local Server" from the left menu bar. In the
        properties section, you will find the option "IE Enhanced Security Configuration".
    </p>

    <img src="/assets/img/config/devops_and_security/creating_virtual_machines_in_virtualbox/visual_studio_code/visual_studio_code_1.png" alt="" title="">

    <p>Click on "on" and turn off this extra protection for administrators and normal users.</p>

    <img src="/assets/img/config/devops_and_security/creating_virtual_machines_in_virtualbox/visual_studio_code/visual_studio_code_2.png" alt="" title="">

    <p>
        On the server, you can download "Visual Studio Code": <a href="https://code.visualstudio.com/download">Download
            link</a>
    </p>

    <p>Download "system installer 64 bit".</p>

    <img src="/assets/img/config/devops_and_security/creating_virtual_machines_in_virtualbox/visual_studio_code/visual_studio_code_3.png" alt="" title="">

    <p>Start the installation. Choose "I accept the agreement" and click on "next".</p>

    <img src="/assets/img/config/devops_and_security/creating_virtual_machines_in_virtualbox/visual_studio_code/visual_studio_code_4.png" alt="" title="">

    <p>Keep the default location for the installation and click "next".</p>

    <img src="/assets/img/config/devops_and_security/creating_virtual_machines_in_virtualbox/visual_studio_code/visual_studio_code_5.png" alt="" title="">

    <p>Click back on "next" to continue.</p>

    <img src="/assets/img/config/devops_and_security/creating_virtual_machines_in_virtualbox/visual_studio_code/visual_studio_code_6.png" alt="" title="">

    <p>Select all options in the window below and click "next".</p>

    <img src="/assets/img/config/devops_and_security/creating_virtual_machines_in_virtualbox/visual_studio_code/visual_studio_code_7.png" alt="" title="">

    <p>When you click "finish", you can start Visual Studio Code.</p>

    <img src="/assets/img/config/devops_and_security/creating_virtual_machines_in_virtualbox/visual_studio_code/visual_studio_code_8.png" alt="" title="">

    <p>This editor allows you to use PowerShell, C sharp, PHP, Python, ...</p>

    <img src="/assets/img/config/devops_and_security/creating_virtual_machines_in_virtualbox/visual_studio_code/visual_studio_code_9.png" alt="" title="">
</div>
