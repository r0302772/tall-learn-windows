    <h3>3.6 Close</h3>
    <p>When you close a virtual machine you have the choice between a number of different options.</p>

    <img src="/assets/img/config/devops_and_security/creating_virtual_machines_in_virtualbox/close.png" alt="" title="">

    <p>
        The top option saves the state of the machine. After that, the virtual computer will not use any more processor
        time or ram, but it will take up more space on your hard drive than if you just shut it down.
    </p>

    <p>
        If you send the shutdown signal to your virtual machine it will shut down cleanly. This is the best option if
        you're tired of working for this course (for a while) or if you're going to close your laptop and take it with
        you.
    </p>

    <p>
        You only turn off the machine if the machine itself is frozen and no longer responds to the shutdown signal.
        Consistently turning off virtual machines guarantees broken virtual machines in the long run.
    </p>

