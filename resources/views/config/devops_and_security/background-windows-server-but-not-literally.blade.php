    <h2>1 Background: Windows Server but not literally.</h2>

    <h3>1.1 Technologies</h3>
    <p>In this course, we will work with these technologies:</p>
    <ul>
        <li>Authentication protocols (such as OAuth 2.0). Create and support secure applications</li>
        <li>Network support, configuration, and development in a DevOps IT world</li>
    </ul>
    <p>
        In the first 6 weeks of the course, you will familiarize yourself with the basic concepts to apply these
        technologies. After that, you will work out your own solution for a case study. You have the freedom to choose
        from a number of different cases. Some are more focused on network management, some are more focused on DevOps
        and developers. This way, you have the possibility to make a free choice that suits your interests.
    </p>

    <h3>1.2 Target audience</h3>
    <p>
        Students who later develop their own programs, sites, apps that require authentication. Using a platform that
        supports Continuous Integration and Continuous Development (CI/DC), you can efficiently create code, test it,
        and bring it into production. Azure Devops is an example of such a platform. Students who support a server
        environment in which such applications are developed. These students efficiently and securely configure the
        server environment required by developers.
    </p>

    <h3>1.3 Prior knowledge</h3>
