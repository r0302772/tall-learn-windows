    <h3>3.2 Installing Windows</h3>

    <p>
        The installation of the different versions of Windows is very similar. However, there are small differences in both installations that we are going to do. So it's important in this section to pay close attention to what needs to be done where.
    </p>

    <p>
        In this part, 2 full windows will be installed, a process that you won't be able to interrupt easily. So make sure you only start it if your computer can stay up for another hour. Running the 2 installations together will only slow things down. Finish them one by one.
    </p>

    <ul>
        <li>We start with the installation of the DC (Windows Server 2019). After that, we will install Windows 10.</li>
        <li>We keep the "language to install" on "English (United States)," but make sure that the "Time and currency format" is set to Dutch (Belgium). This will also change the keyboard to "Belgian (Period)," or AZERTY. If your keyboard is a different one, make sure it is set correctly.</li>
    </ul>

    <p>
        Link the correct ISO to the 2 virtual machines. You do this when starting up the virtual machines. In the pop-up that appears, you can use the browse button to go to the right ISO file. (In our example, this was c:\Sources)
    </p>

