    <h4>3.4.2 Registration</h3>
    <p>You can register a Windows version in 2 ways:</p>
    <ol>
        <li><strong>Trial version with a period of XX days:</strong> Registered version without the limitation of the
            usage period
        </li>
    </ol>
    <p>
        We work with a trial version. This version "activates" itself automatically as soon as there is an internet
        connection. A random trial key is used. So you don't need a license key. The activation makes sure your "trial
        period" starts and starts counting down.
    </p>
    <p>
        Here you do the registration manually and enter a license key. If the activation is not done, then you will
        notice that the device will reboot automatically every so often. At the end of your trial period, your device
        will continue to work but will reboot at random intervals.
    </p>

    <p>
        Via "Control Panel", "System and Security", and "System", you can check if the "activation" has happened. If you
        see "Windows is activated" then everything is fine. (If not, then you probably have problems connecting to the
        internet). You only need the option "change product key" if you wish to use your VM for a longer period. For
        this course, the trial period is sufficient.
    </p>

    <img src="/assets/img/config/devops_and_security/creating_virtual_machines_in_virtualbox/registration.png" alt="" title="">

    <p>Check on both virtual machines if the activation is OK before proceeding.</p>
