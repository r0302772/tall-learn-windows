        <h2 class="text-2xl">1.1. What's PowerShell?</h2>
        <p>
            Windows is a graphical operating system where you start and control activities with the mouse. To keep the
            interface nice and tidy, only a limited number of functions are directly accessible. For other functions,
            you need to dive deeper into the system.
        </p>

        <p>
            Often you will have to perform several actions to configure or activate 1 specific function or task.
        </p>

        <p>
            The same result, but in a faster and easier way can be achieved via PowerShell.
        </p>

        <p>
            PowerShell is the Windows command-line interface where you use text commands in the operating system. With
            Windows PowerShell, you can simplify complex command-line operations.
        </p>

        <p>For example, in PowerShell, you can obtain system information much faster than via the Windows graphical user
            interface, e.g. an overview of the network cards, the MAC addresses, and the IP configuration. In
            PowerShell, it is one command (cmdlet), in Windows, you will only get the same result after many clicks and
            windows to open and close.
        </p>

        <p>
            In addition, you can always filter or further process the output of PowerShell in the next command. An
            overview of all shared folders, an overview of the processes, ..., it's all available in PowerShell with one
            command.
        </p>
