    <h3>1.1.e. Opening Shortcuts with "Run as Admin"</h3>

    <p><strong>RUN as admin!!!</strong> (Right mouse button, both for cmd.exe and for starting PowerShell from the start
        menu)</p>
    <p>Otherwise, you will end up in your personal folder and have only limited user rights, no admin rights. The
        technology behind this is <b>"UAC"</b> or <b>"User Account Control"</b>. Later on, we will explain what this
        technology
        is.</p>
