    <h3>1.1.d. Windows PowerShell Editors</h3>

    <p class="underline font-bold">You have the choice between 3 editors:</p>

    <ol>
        <li>
            <p>A first option is to boot <b>Windows PowerShell</b> directly from the Windows 10 home screen. Type in
                "Powershell.exe" and then click on "Windows Powershell" and open Powershell as admin (right-click on
                application: "run as administrator").</p>
            <p><u><b>1) Windows 10: Powershell.exe:</b></u></p>
            <p>Type in Powershell:</p>
            <img src="/assets/img/windows_powershell/introduction/windows_powershell_editors_1.png" alt="" title="">
            <img src="/assets/img/windows_powershell/introduction/windows_powershell_editors_2.png" alt="" title="">
        </li>

        <li>
            <p>A second possibility is to install <b>Visual Studio Code</b> and use it as a versatile editor. This editor can
                be used as an alternative to Visual Studio. The editor contains a number of addons so you can write
                Python, HTML, css, javascript, C sharp, ....</p>
            <p><u><b>2) Windows 10: Visual Code Editor</b></u></p>
            <img src="/assets/img/windows_powershell/introduction/windows_powershell_editors_3.png" alt="" title="">
        </li>

        <li>
            <p>A third possibility is via the <b>Windows Powershell ISE</b>. This is the original powershell editor. We use it
                mainly to create scripts. Soon it will be replaced by Visual Studio Code editor. <b>This is outdated and
                    we
                    will not use it. We will immediately switch to Visual Studio Code for our exercises.</b></p>
            <p><u><b>3) Windows 10: Powershell ISE:</b></u></p>
            <img src="/assets/img/windows_powershell/introduction/windows_powershell_editors_4.png" alt="" title="">
            <img src="/assets/img/windows_powershell/introduction/windows_powershell_editors_5.png" alt="" title="">
        </li>
    </ol>

    <p><strong><u>Note:</u></strong></p>

    <p>1. Do not confuse these windows with the command prompt of Windows. You start it up via cmd.exe and it looks like
        this:</p>

    <img src="/assets/img/windows_powershell/introduction/windows_powershell_editors_6.png" alt="" title="">

    <p>Powershell cmdlets cannot be executed in the command prompt, and it is intended that the powershell prompt will
        eventually replace the command prompt.</p>

